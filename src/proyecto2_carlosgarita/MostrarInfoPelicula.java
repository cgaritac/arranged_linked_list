    //Método para cargar los datos de la lista enlazada en la tabla
    private void cargarTabla() {
        
        //Definición de variables locales
        String[] fila = new String[2];
        int numPelicula = 0;

        //ciclo para recorrer la lista enlazada y llenar las filas de la tabla
        for (int i = 0; i < listaPeliculas.longitud(); i++) {
            numPelicula++;
            fila[0] = String.valueOf(numPelicula);
            fila[1] = listaPeliculas.obtener(i).getNombre();

            modeloTabla.addRow(fila); //Se adicionan los valores del array "fila" a una fila del modelo de la tabla  
        }
    }

    //Método para cargar los datos de la pelicula seleccionada de la lista enlazada en las cajas de texto en pantalla
    private void cargarDatos(int indice) {

        //Asignación de los valores de la película seleccionada a los cuadros de texto
        this.txtNombre.setText(listaPeliculas.obtener(indice).getNombre());
        this.txtGenero.setText(listaPeliculas.obtener(indice).getGenero());
        this.txtDuracion.setText(String.valueOf(listaPeliculas.obtener(indice).getDuracion()));
    }

