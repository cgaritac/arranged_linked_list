    private void btnMostrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMostrarActionPerformed
        
        //Definición de variable local con iniciación de valor mediante el spinner
        int numPelicula = Integer.parseInt(this.spiNumPelicula.getValue().toString());

        //Condicional en caso de que no se haya seleccionado la fila en la que se encuentra la película
        if (this.spiNumPelicula.getValue().toString().equals("0")) {
            JOptionPane.showMessageDialog(this,
                    "Es necesario que indique el número de la película a mostrar antes de continuar",
                    "Alto",
                    JOptionPane.WARNING_MESSAGE);
            
          //Condicional en caso de que la tabla esté vacía    
        } else if (this.tbPeliculasAgregadas.getRowCount() == 0) {
            JOptionPane.showMessageDialog(this,
                    "No hay registros de películas",
                    "Alto",
                    JOptionPane.WARNING_MESSAGE);

          //Condicional en caso de que el número de película seleccionado sea incorrecto    
        } else if (listaPeliculas.longitud() < numPelicula) {
            JOptionPane.showMessageDialog(this,
                    "La película número " + this.spiNumPelicula.getValue().toString() + " no se encuentra en la lista",
                    "Alto",
                    JOptionPane.WARNING_MESSAGE);
        } else {
            int indice = numPelicula - 1; //Definición de variable local
            cargarDatos(indice); //Llama al método cargarDatos
        }
    }//GEN-LAST:event_btnMostrarActionPerformed

